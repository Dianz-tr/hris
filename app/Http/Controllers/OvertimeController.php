    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $ov = new Overtime();
        $ov->employee_id = $request->get('employee_id');
        $ov->ot_date = $request->get('ot_date');
        $ov->ot_hours = $request->get('ot_hours');
        $ov->description = $request->get('description');
        $ov->approved_by = $request->get('approved_by');
        $ov->save();

        return redirect()->back();
    }

